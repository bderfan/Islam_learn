               <br>
               <h4 style="line-height:1.5;">হজ্জের দ্বিতীয় দিন ৯ জিলহজ আরাফায় অবস্থান ফরজ । ফজরের নামাজ মিনায় পড়ে আরাফার ময়দানের দিকে রওনা করতে হয় । প্রয়োজনে ফজরের আগে রাতেও আরাফার উদ্দেশে রওনা করা যাবে । আরাফার ময়দানে দুপুর ১২টার পর থেকে সূর্যাস্ত পর্যন্ত অবস্থান করতে হবে । আরাফার ময়দানে দোয়া কবুল হয় । সুতরাং এ সময় সবাইকে দোয়ায় মগ্ন থাকা উচিত । আরাফার ময়দানে জাবালে রহমতের কাছাকাছি অবস্থান করা ভালো । যোহর এবং আসরের নামাজ মসজিদে নামিরায় জামাতের সঙ্গে নির্দিষ্ট শর্তানুসারে আদায় করা উত্তম । তবে ওই জামাতে শরিক হওয়া সম্ভব না হলে যথাসময়ে যোহরের ওয়াক্তে যোহর এবং আসরের ওয়াক্তে আসর নিজ নিজ তাঁবুতে আজান-ইকামতসহকারে জামাআতের সঙ্গে পড়ুন ।</h4>
