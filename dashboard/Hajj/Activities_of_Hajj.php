               <h3 class="fw-bold">[পাথর নিক্ষেপ, কুরবানি, চুল মুণ্ডন ও তাওয়াফ]</h3>
               <br>
               <h4 style="line-height:1.5;">হজ্জের তৃতীয় দিন ১০ জিলহজ মিনায় পৌঁছার পর এ দিনের চারটি কাজ ধারাবাহিকভাবে পালন করতে হবে-</h4>
               <br>
               <h4><span class="fw-bold">পাথর নিক্ষেপ করাঃ</span> এই দিনের প্রথম কাজ হলো জামারায় আকাবায় গিয়ে সাতটি পাথর নিক্ষেপ করা (ওয়াজিব) । এটাকে জামারাতুল কুবরাও বলা হয়, আবার 'বড় শয়তান' বলেও প্রসিদ্ধ আছে । মিনায় পাথর নিক্ষেপের তিনটি স্থান রয়েছে । আগে ওই তিনটি স্থানে তিনটি স্তম্ভ বা পিলার ছিল বর্তমানে পিলারের জায়গায় ৪০ ফুট দৈর্ঘ্য উঁচু দেওয়াল তৈরি করে দেওয়া হয়েছে, হাজি সাহেবদের সুবিধার জন্য নিচতলা, দ্বিতীয় তলায় এবং তৃতীয় তলায় রাস্তা করা হয়েছে । যাতে একই সঙ্গে অসংখ্য লোক পাথর নিক্ষেপ করতে পারেন । পাথর নিক্ষেপের নিয়ম হচ্ছে- পাথর নিক্ষেপের সময় মিনাকে ডান দিকে রেখে দাঁড়ান । তর্জনী ও বৃদ্ধাঙ্গুলি দ্বারা পাথর ধরে নিক্ষেপ করুন । প্রথম পাথর নিক্ষেপের পূর্বমুহূর্ত থেকে তালবিয়া পড়া বন্ধ করে দিতে হবে । পাথর নিক্ষেপের সময় বলুন : বিসমিল্লাহি আল্লাহু আকবার ।</h4>
               <br>
               <h4><span class="fw-bold">কুরবানি করাঃ</span> এই দিনের দ্বিতীয় কাজ হলো দমে শোকর বা হজ্জের শোকরিয়া স্বরূপ কুরবানি করা (ওয়াজিব) । নিজ হাতে করুন কিংবা কাউকে পাঠান; কিন্তু জবেহ হয়ে যাওয়ার ব্যাপারে নিশ্চিত হোন । কিরান ও তামাত্তু হজ্জ পালনকারীদের জন্য এটা ওয়াজিব । আর ইফরাদ হজ্জ পালনকারীদের জন্য মুস্তাহাব । মিনায় কুরবানি করতে না পারলে মক্কা শহরেও কুরবানি করে নিতে পারেন ।</h4>
               <br>
               <h4><span class="fw-bold">চুল মুণ্ডন বা কর্তন করাঃ</span> এই দিনের তৃতীয় কাজ হলো হলক বা কসর করা । (চুল মুণ্ডন বা কর্তন) এটি ওয়াজিব । কুরবানি করার পর পুরো মাথার চুল মুণ্ডন করে ফেলুন । মুণ্ডনকারীদের জন্য হুজুর পাক (সাঃ) তিনবার দোয়া করেছেন । তাই এতে ফজিলত বেশি ।</h4>
               <br>
               <h4><span class="fw-bold">তাওয়াফে জিয়ারতঃ</span> এ দিনের চতুর্থ কাজ হলো তাওয়াফে জিয়ারত (ফরজ) একে 'তাওয়াফে ইফাযা'ও বলা হয় : এটা হজ্জের শেষ রুকন । মিনায় উপরোক্ত কাজগুলো সেরে হাজিগণ মক্কা শরিফে গিয়ে তাওয়াফ-ই-জিয়ারত করবেন । এ তাওয়াফে ইজতিবা নেই । ১০ তারিখে সম্ভব না হলে ১১ বা ১২ তারিখের সূর্যাস্তের আগে অবশ্যই এ তাওয়াফ করতে হবে । যারা মক্কা থেকে ৮ জিলহজ আসার আগে একটি নফল তাওয়াফের সঙ্গে সায়ী করে আসেনি তাওয়াফে জিয়ারতে তাদের অবশ্যই সায়ী করতে হবে । তাওয়াফে জিয়ারতের কোনো বদলা নেই, এ তাওয়াফ করতেই হবে । এর কোনো বিকল্প নেই ।</h4>
