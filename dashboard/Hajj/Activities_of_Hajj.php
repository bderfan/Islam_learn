               <h4 style="line-height:1.5;">১১ জিলহজ মিনায় রাতযাপন সুন্নত । এদিন মিনায় তিন শয়তানকে পাথর মারা ওয়াজিব । দুপুরের পর প্রথমে জামারায়ে সুগরা, (মসজিদে খাইফের সন্নিকটে) অতঃপর জামারায়ে উসতা, সর্বশেষ জামারায়ে আকাবায় ৭টি করে মোট ২১টি পাথর নিক্ষেপ করতে হবে । প্রত্যেকটি পাথর নিক্ষেপের সময় তাকবির বলবেন ।</h4>
