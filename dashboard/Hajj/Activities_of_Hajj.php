               <h4 style="text-indent: 50px; line-height:1.5;">মক্কায় পৌঁছার পর বিদায়ী তাওয়াফ ছাড়া হজ্জের আর কোনো জরুরি কাজ বাকি নেই । হজ্জ আদায়ের তাওফিকদানের জন্য আল্লাহ পাকের শোকর, নফল তাওয়াফ, উমরাহ ও অন্যান্য ইবাদত করতে থাকুন ।</h4>
