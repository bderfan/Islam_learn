               <h3 class="fw-bold mt-5">ফেরেশতাদের সামনে হাজীদের প্রশংসাঃ</h3>
               <br>
               <h2 style="line-height:1.8">عَنْ عَائِشَةَ قَالَتْ أَنَّ رَسُولَ اللهِ صلى الله عليه وسلم قَالَ مَا مِنْ يَوْمٍ أَكْثَرَ مِنْ أَنْ يَعْتِقَ اللهُ عَزَّ وَجَلَّ فِيْهِ عَبْدًا أَوْ أَمَةً مِنَ النَّارِ مِنْ يَوْمِ عَرَفَةَ إِنَّهُ لَيَدْنُو ثُمَّ يُبَاهِىْ بِهِمُ الْمَلاَئِكَةَ وَيَقُوْلُ مَا أَرَادَ هَؤُلاَءِ ?</h2>
               <br>
               <h4 style="line-height:1.5;">আয়েশা (রাঃ) হ'তে বর্ণিত তিনি বলেন, রাসূল (সাঃ) বলেছেন, 'আরাফার দিন ব্যতীত অন্য কোন দিন আল্লাহ এত অধিক পরিমাণ লোককে জাহান্নাম থেকে মুক্ত করেন না । ঐদিন আল্লাহ নিকটবর্তী হন । অতঃপর আরাফাহ ময়দানের হাজীদের নিয়ে ফেরেশতাদের নিকট গর্ব করেন ও বলেন, দেখ ঐ লোকেরা কি চায় ?' (মুসলিম হা/১৩৪৮; মিশকাত হা/২৫৯৪; ছহীহাহ হা/২৫৫১) । অন্য বর্ণনায় এসেছে, 'ওরা আল্লাহর মেহমান । আল্লাহ ওদের ডেকেছেন তাই ওরা এসেছে । এখন ওরা চাইবে আর আল্লাহ তা দিয়ে দিবেন ।' (ইবনু মাজাহ হা/২৮৯৩; ছহীহাহ হা/১৮২০) । রাসূলুল্লাহ (সাঃ) বলেন, خَيْرُ الدُّعَاءِ دُعَاءُ يَوْمِ عَرَفَةَ 'শ্রেষ্ঠ দোয়া হ'ল আরাফাহ দিবসের দোয়া ।' (তিরমিযী হা/৩৫৮৫; মিশকাত হা/২৫৯৮; ছহীহ আত-তারগীব হা/১৫৩৬; ছাহীহাহ হা/১৫০৩)</h4>
