               <br>
               <h3 class="fw-bold mt-5">হজ্জের নিয়তকারীগণ কোন কারণে হজ্জ করতে সক্ষম না হ'লেও নেকী পাবেঃ</h3>
               <br>
               <h2 style="line-height:1.8">عَنْ أَبِي هُرَيْرَةَ قَالَ قَالَ رَسُولُ اللهِ صَلَّى اللهُ عَلَيْهِ وَسَلَّمَ مَنْ خَرَجَ حَاجًّا أَوْ مُعْتَمِرًا أَوْ غَازِيًا ثُمَّ مَاتَ فِيْ طَرِيْقِهِ كَتَبَ اللهُ لَهُ أَجْرَ الْغَازِيْ وَالْحَاجِّ وَالْمُعْتَمِرِ إِلَى يَوْمِ الْقِيَامَةِ  .</h2>
               <br>
               <h4 style="line-height:1.5;">আবু হুরায়রা (রাঃ) হ'তে বর্ণিত তিনি বলেন, রাসূলুল্লাহ (সাঃ) এরশাদ করেছেন, 'যে ব্যক্তি হজ্জ, ওমরাহ কিংবা জিহাদের উদ্দেশ্যে বের হ'ল এবং রাস্তায় মৃত্যুবরণ করল, আল্লাহ তার জন্য পূর্ণ নেকী লিখে দিবেন ।' (বায়হাকী, শু‘আবুল ঈমান হা/৪১০০; ছহীহ আত-তারগীব হা/১১১৪; ছহীহাহ হা/২৫৫৩ ; মিশকাত হা/২৫৩৯)</h4>
               <br>
               <br>
               <br>
               <h3 class="fw-bold mt-5">হজ্জে মৃত্যুবরণকারীগণ ক্বিয়ামতের দিন তালবিয়া পাঠরত অবস্থায় উঠবেঃ</h3>
               <br>
               <h2 style="line-height:1.8">عَنِ ابْنِ عَبَّاسٍ رضى الله عنهما قَالَ بَيْنَمَا رَجُلٌ وَاقِفٌ مَعَ رَسُولِ اللهِ صلى الله عليه وسلم بِعَرَفَةَ إِذْ وَقَعَ مِنْ رَاحِلَتِهِ فَأَقْصَعَتْهُ أَوْ قَالَ فَأَقْعَصَتْهُ، فَقَالَ رَسُولُ اللهِ صلى الله عليه وسلم اغْسِلُوْهُ بِمَاءٍ وَسِدْرٍ وَكَفِّنُوهُ فِىْ ثَوْبَيْنِ وَلاَ تُحَنِّطُوْهُ وَلاَ تُخَمِّرُوْا رَأْسَهُ فَإِنَّ اللهَ يَبْعَثُهُ يَوْمَ الْقِيَامَةِ مُلَبِّيًا .</h2>
               <br>
               <h4 style="line-height:1.5;">ইবনু আববাস (রাঃ) হ'তে বর্ণিত তিনি বলেন, 'এক ব্যক্তি আল্লাহর রাসূল (সাঃ) এর সঙ্গে আরাফাতে অবস্থান কালে অকস্মাৎ সে তার সওয়ারী হ'তে পড়ে যান । এতে তাঁর ঘাড় মটকে গেল অথবা রাবী বলেন, ঘাড় মটকে দিল । (যাতে তিনি মারা গেলেন) । তখন আল্লাহর রাসূল (সাঃ) বললেন, তাঁকে বরই পাতাসহ পানি দিয়ে গোসল দাও এবং দু'কাপড়ে তাঁকে কাফন দাও; তাঁকে সুগন্ধি লাগাবে না এবং তার মস্তক আবৃত করবে না । কেননা আল্লাহ তা'আলা কিয়ামতের দিন তাঁকে তালবিয়া পাঠরত অবস্থায় উত্থিত করবেন ।' (বুখারী হা/১২৬৬; মুসলিম হা/১২০৬; মিশকাত হা/১৬৩৭)</h4>
               <br>
               <br>
               <br>
               <h3 class="fw-bold mt-5">হাজীদের প্রতিটি পদচারণায় নেকী অর্জিত হয় ও গুনাহ বিদূরীত হয়ঃ</h3>
