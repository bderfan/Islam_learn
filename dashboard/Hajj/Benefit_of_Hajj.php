               <h3 class="fw-bold mt-5">হাজীগণ আল্লাহর মেহমানঃ</h3>
               <br>
               <h2 style="line-height:1.8">عَنْ أَبِيْ هُرَيْرَةَ قَالَ قَالَ رَسُولُ اللهِ صلى الله عليه وسلم وَفْدُ اللهِ ثَلَاثَةٌ الْغَازِيْ، وَالْحَاجُّ وَالْمُعْتَمِرُ .</h2>
               <br>
               <h4 style="line-height:1.5;">আবু হুরায়রা (রাঃ) হ'তে বর্ণিত তিনি বলেন, রাসূল (সাঃ) বলেছেন, 'আল্লাহর মেহমান হ'ল তিনটি দল- আল্লাহর রাস্তায় যুদ্ধকারী, হজ্জকারী ও ওমরাহ্কারী ।' (নাসাঈ হা/২৬২৫; ছহীহুল জামে‘ হা/৭১১২; ছহীহ আত-তারগীব হা/১১০৯; মিশকাত হা/২৫৩৭) । অন্য বর্ণনায় রয়েছে, রাসূল (সাঃ) বলেন, . الْحُجَّاجُ وَالْعُمَّارُ وَفْدُ اللهِ إِنْ دَعَوْهُ أَجَابَهُمْ وَإِنِ اسْتَغْفَرُوهُ غَفَرَ لَهُمْ 'হজ্জ ও ওমরাহ পালনকারীগণ আল্লাহর মেহমান । তারা দোয়া করলে তিনি কবুল করেন । তারা ক্ষমা প্রার্থনা করলে তিনি তাদের ক্ষমা করে দেন ।' (ইবনু মাজাহ হা/২৮৯২; ছহীহ আত-তারগীব হা/১১০৯; মিশকাত হা/২৫৩৬) । অপর বর্ণনায় রয়েছে, 'তারা কোন কিছু চাইলে তিনি তা দেন ।' (ইবনু মাজাহ হা/২৮৯৩; ছহীহাহ হা/১৮২০; ছহীহুল জামে‘ হা/৪১৭১; ছহীহ আত-তারগীব হা/১১০৭)</h4>
