               <h4 style="line-height:1.5;">উম্মুল মুমিনীন আয়েশা (রাঃ) হ'তে বর্ণিত তিনি বলেন, আমি বললাম, হে আল্লাহর রাসূল! আমরা কি আপনাদের সঙ্গে যুদ্ধ ও জিহাদে অংশগ্রহণ করব না ? তিনি বললেন, তোমাদের জন্য উত্তম ও উৎকৃষ্ট জিহাদ হ'ল হজ্জ, কবূল হজ্জ । আয়েশা (রাঃ) বললেন, আল্লাহর রাসূল (সাঃ) হ'তে এ কথা শোনার পর আমি আর কখনো হজ্জ ছাড়ব না ।' (বুখারী হা/১৮৬১; আহমাদ হা/২৫৫৮৪১; মিশকাত হা/২৫৩৪) । অন্য বর্ণনায় রয়েছে, আয়েশা (রাঃ) একদা রাসূলুল্লাহ (সাঃ) কে জিজ্ঞেস করলেন, হে আল্লাহর রাসূল! মহিলাদের উপরে 'জিহাদ' আছে কি ? তিনি বললেন, হ্যাঁ আছে । তবে সেখানে যুদ্ধ নেই । সেটি হ'ল হজ্জ ও ওমরাহ ।' (ইবনু মাজাহ হা/২৯০১; মিশকাত হা/২৫৩৪; ইরওয়া হা/৯৮১;ছহীহ আত-তারগীব হা/১০৯৯) । অপর এক বর্ণনায় এসেছে, রাসূল (সাঃ) বলেন, . جِهَادُ الْكَبِيْرِ وَالصَّغِيْرِ وَالضَّعِيْفِ وَالْمَرْأَةِ الْحَجُّ وَالْعُمْرَةُ 'বড়, ছোট, দুর্বল ও মহিলা সকলের জন্য জিহাদ হ'ল হজ্জ ও ওমরাহ ।' (নাসাঈ হা/২৬২৬; ছহীহ আত-তারগীব হা/১১০০) । অন্য বর্ণনায় রয়েছে, এক ব্যক্তি রাসূল (সাঃ) এর নিকট এসে বলল, হে আল্লাহর রাসূল! إِنِّي جَبَانٌ، وَإِنِّيْ ضَعِيفٌ، فَقَالَ: هَلُمَّ إِلَى جِهَادٍ لَا شَوْكَةَ فِيْهِ: الْحَجِّ 'আমি তো ভীতু এবং দুর্বল (আর আমার উপর জিহাদ ফরজ) । তখন রাসূল (সাঃ) বললেন, ছুটে এসো এমন এক জিহাদের দিকে যেখানে কোন কষ্ট নেই । আর তা হ'ল হজ্জ ।' (মু'জামুল কাবীর হা/২৯১০, আওসাত্ব হা/৪২৮৭; ছহীহুল জামে' হা/৭০৪৪; ছহীহ আত-তারগীব হা/১০৯৮)</h4>
