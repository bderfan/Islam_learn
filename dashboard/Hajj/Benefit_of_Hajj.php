               <h3 class="fw-bold mt-5">অন্যতম শ্রেষ্ঠ আমল হজ্জঃ</h3>
               <br>
               <h2 style="line-height:1.8">عَنْ أَبِى هُرَيْرَةَ رضى الله عنه قَالَ سُئِلَ النَّبِىُّ صلى الله عليه وسلم أَىُّ الأَعْمَالِ أَفْضَلُ؟ قَالَ إِيْمَانٌ بِاللهِ وَرَسُوْلِهِ. قِيْلَ ثُمَّ مَاذَا قَالَ جِهَادٌ فِىْ سَبِيْلِ اللهِ. قِيْلَ ثُمَّ مَاذَا قَالَ حَجٌّ مَبْرُوْرٌ .</h2>
               <br>
               <h4 style="line-height:1.5;">আবু হুরায়রা (রাঃ) হ'তে বর্ণিত তিনি বলেন, রাসূল (সাঃ) কে জিজ্ঞেস করা হ'ল, কোন আমলটি সর্বোত্তম ? তিনি বললেন, 'শ্রেষ্ঠ আমল হ'ল  আল্লাহ ও তাঁর রাসূলের উপরে ঈমান আনা । বলা হ'ল, তারপর কি ? তিনি বললেন, আল্লাহর রাস্তায় জিহাদ করা । জিজ্ঞেস করা হ'ল, তারপর কি ? তিনি বললেন, কবুল হজ্জ ।' (বুখারী হা/১৫১৯; মুসলিম হা/৮৩; মিশকাত হা/২৫০৬) । অন্য বর্ণনায় রয়েছে, রাসূল (সাঃ) বলেন, . الإِيْمَانُ بِاللهِ وَحْدَهُ ثُمَّ الْجِهَادُ ثُمَّ حَجَّةٌ بَرَّةٌ تَفْضُلُ سَائِرَ الْعَمَلِ كَمَا بَيْنَ مَطْلَعِ الشَّمْسِ إِلَى مَغْرِبِهَا 'শ্রেষ্ঠ আমল হ'ল এক আল্লাহর প্রতি ঈমান আনা । অতঃপর আল্লাহর রাস্তায় জিহাদ করা । অতঃপর কবুল হজ্জ । যা সকল আমলের উপর এমন মর্যাদাবান যেমন পূর্ব দিগন্ত ও পশ্চিম দিগন্তের মধ্যে দূরত্ব রয়েছে ।' (আহমাদ হা/১৯০৩২; মু‘জামুল কাবীর হা/৮০৯; ছহীহুল জামে‘ হা/১০৯১; ছহীহ আত-তারগীব হা/১১০৩)</h4>
